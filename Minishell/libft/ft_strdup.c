#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*d;

	d = (char *) ft_malloc ((ft_strlen(s) + 1) * sizeof(char));
	if (!d)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
